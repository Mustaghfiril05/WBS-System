    <h4 style="text-align: left; color:#16a085">Definisi Pelapor, Stakeholder & Terlapor :</h4>
     <ul style="text-align: justify;">
        <li>Pelapor (Whistle Blower) adalah perseorangan atau badan hoUkum baik dari pihak internal maupun stakeholder lainnya yang melaporkan adanya indikasi pelanggaran    melalui media yang telah disediakan oleh perusahaan.</li>
        <li>Stakeholder adalah pihak yang mempunyai kepentingan terhadap keberlangsungan operasi perusahaan seperti pelanggan, pemasok kontraktor, regulator, masyarakat    dilingkungan tempat bekerja, karyawan, manajemen dan pemerintah sebagai pemegang saham..</li>
        <li>Terlapor adalah pegawai/Direksi/Komisaris perusahaan dan atau pegawai perusahaan yang ditugaskan di anak perusahaan dan atau perusahaan afiliasi, serta personil lainnya yang bekerja untuk dan atas nama perusahaan..</li>
     </ul>
    
        </div>
    </div>

    <div class="row">
          <div class="col-lg-12">
            <h4 style="text-align: left; color:#16a085">Syarat / Kriteria pengaduan dugaan pelanggaran WBS ALP :</h4>
              
                  <ul style="text-align: justify;"> 
                      <li>Identitas pelapor :   Pelapor melampirkan bukti identitas secara jelas dan masih berlaku.</li>
                      <li>Permasalahan  : Pelanggaran yang diadukan merupakan pelanggaran yang terkait dengan aktifitas perusahaan.</li>
                      <li>Lokasi kejadian   : Pelanggaran yang dilakukan terjadi pada lingkungan perusahaan.</li>
                      <li>Waktu kejadian  : Periode terjadinya pelanggaran disebutkan dengan jelas antara lain meliputi tanggal, bulan, tahun serta terjadi pada saat terlapor masih berstatus aktif di perusahaan.</li>
                      <li>Terlapor  : Identitas dan status kepegawaian terlapor harus jelas.</li>
                      <li>Menjelaskan terjadinya pelanggaran yang dilakukan oleh terlapor yang berakibat pada kerugian materiil atau berpengaruh negative terhadap citra perusahaan.</li>
                      <li>Pengaduan harus dilengkapi dengan bukti yang cukup dan memadai (tidak terbatas pada data, dokumen, gambar, rekaman audio, visual berbentuk cetak atau elektronik atau data informasi lainnya) yang memperkuat fakta terjadinya pelanggaran.</li>
                      <li>Pengaduan harus disampaikan melalui media pengaduan yang sudah disediakan oleh perusahaan.</li>

                  </ul>
                 
          </div>
      </div>


    <div class="row">
          <div class="col-lg-12">
    <h4 style="text-align: left; color:#16a085"> Perlindungan Pelapor :</h4>
    <ul style="text-align: justify;list-style-type:none">
     <li>1.  Pelapor yang membuat laporan pengaduan terjadinya pelanggaran dijamin kerahasiaan identitas dirinya oleh Direksi Perusahaan maupun Tim Penanganan Pengaduan      Pelanggaran (TP3)..</li>
     <li>2.  Perusahaan memberikan perlindungan kepada pelapor atas kemungkinan dilakukannya hal-hal sebagai berikut :</li>
      <ul>
        <li>Pemecatan yang tidak adil..</li>
        <li>Penurunan jabatan atas pangkat dengan alasan yang tidak jelas..</li>
        <li>Catatan yang merugikan dalam file data pribadinya (personal file record)..</li>
      </ul>
     <li>3.  Pelapor tidak dapat dijatuhi sanksi/hukuman atau diperiksa telah melakukan pelanggaran disiplin pegawai atas pengaduan yang sedang atau telah dilakukannya sepanjang     pengaduannya memenuhi kriteria dan dapat dipertanggungjawabkan.</li>
   </ul>
