    <h4 style="text-align: left;">Definisi Pelapor, Stakeholder & Terlapor :</h4>
    1.  Pelapor (Whistle Blower) adalah perseorangan atau badan hoUkum baik dari pihak internal maupun stakeholder lainnya yang melaporkan adanya indikasi pelanggaran    melalui media yang telah disediakan oleh perusahaan.<br>
    2.  Stakeholder adalah pihak yang mempunyai kepentingan terhadap keberlangsungan operasi perusahaan seperti pelanggan, pemasok kontraktor, regulator, masyarakat    dilingkungan tempat bekerja, karyawan, manajemen dan pemerintah sebagai pemegang saham..<br>
    3.  Terlapor adalah pegawai/Direksi/Komisaris perusahaan dan atau pegawai perusahaan yang ditugaskan di anak perusahaan dan atau perusahaan afiliasi, serta personil lainnya yang bekerja untuk dan atas nama perusahaan..<br>
        </div>
    </div>
    <div class="row">
          <div class="col-lg-12">
    <h4 style="text-align: left;"> Perlindungan Pelapor :</h4>
     1.  Pelapor yang membuat laporan pengaduan terjadinya pelanggaran dijamin kerahasiaan identitas dirinya oleh Direksi Perusahaan maupun Tim Penanganan Pengaduan      Pelanggaran (TP3)..<br>
     2.  Perusahaan memberikan perlindungan kepada pelapor atas kemungkinan dilakukannya hal-hal sebagai berikut :<br>
     &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a.  Pemecatan yang tidak adil..<br>
     &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b.  Penurunan jabatan atas pangkat dengan alasan yang tidak jelas..<br>
     &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c.  Catatan yang merugikan dalam file data pribadinya (personal file record)..<br>
     3.  Pelapor tidak dapat dijatuhi sanksi/hukuman atau diperiksa telah melakukan pelanggaran disiplin pegawai atas pengaduan yang sedang atau telah dilakukannya sepanjang     pengaduannya memenuhi kriteria dan dapat dipertanggungjawabkan.
