    <h4 style="text-align: left; color:#16a085">Definisi Pelapor, Stakeholder & Terlapor :</h4>
    <p style="text-align: justify;"> 
        Good Corporate Governance (GCG) atau tata kelola perusahaan yang baik sangat penting bagi sebuah perusahaan untuk memastikan bahwa kepentingan seluruh pihak, baik pemangku kepentingan maupun pemegang saham, telah berjalan dengan baik dan benar serta jauh dari aspek-aspek yang merugikan dalam mencapai tujuan organisasi. <br>
        Berdasarkan Keputusan Menteri BUMN Nomor Kep-117/M-MBU/2002 tentang Penerapan Praktik Good Corporate Governance pada Badan Usaha Milik Negara (BUMN), corporate governance adalah suatu proses dan struktur         yang digunakan oleh organ BUMN untuk meningkatkan keberhasilan usaha dan akuntabilitas perusahaan guna mewujudkan nilai pemegang saham dalam jangka panjang lainnya berlandaskan peraturan perundang-undangan         dan nilai-nilai etika.<br>
        PT ALP Petro Industry,terus berupaya mewujudkan tata kelola perusahaan yang baik dalam setiap aktivitas dan kinerja usahanya. Adanya implementasi GCG yang baik akan mampu        membangun kepercayaan seluruh pemangku kepentingan serta memberikan output terbaik dalam menghadapi berbagai tantangan usaha yang ada. Pemenuhan tata kelola perusahaan yang baik tersebut tidak hanya        terbatas pada bagaimana perusahaan memenuhi dan menginternalisasi prinsip GCG namun juga menjadikan GCG sebagai budaya yang diterapkan secara menyeluruh di setiap tingkatan organisasi maupun jabatan.
        Prinsip tata kelola yang diterapkan oleh PT. ALP Petro Industry mengacu kepada Peraturan menteri Negara BUMN No.PER-01/MBU/2011 tanggal 1 Agustus 2011 juncto No.PER-09/MBU/2012 tanggal 6 Juli 2012 tentang         Penerapan Tata Kelola Perusahaan Yang baik pada Badan Usaha Milik Negara, yakni :<br>

    </p>
      <ul style="text-align: justify;">
          <li>1.  Transparansi (transparency), yaitu keterbukaan dalam melaksanakan proses pengambilan keputusan dan keterbukaan dalam mengungkapkan informasi material dan relevan mengenai perusahaan;</li>
          <li>2.  Akuntabilitas (accountability), yaitu kejelasan fungsi, pelaksanaan dan pertanggungjawaban organ-organ perusahaan sehingga pengelolaan perusahaan terlaksana secara efektif;</li>
          <li>3.  Pertanggungjawaban (responsibility), yaitu kesesuaian di dalam pengelolaan perusahaan terhadap peraturan perundang-undangan yang berlaku dan prinsip-prinsip korporasi yang sehat;</li>
          <li>4.  Kemandirian (independency), yaitu keadaan dimana perusahaan dikelola secara profesional tanpa benturan kepentingan dan pengaruh/tekanan dari pihak manapun yang tidak sesuai dengan peraturan perundang-undangan yang berlaku dan prinsip-prinsip korporasi yang sehat;</li>
          <li>5.  Kewajaran (fairness), yaitu keadilan dan kesetaraan di dalam memenuhi hak-hak Pemangku Kepentingan (stakeholders) yang timbul berdasarkan perjanjian dan peraturan perundang-undangan yang berlaku.</li>
      </ul>
      
